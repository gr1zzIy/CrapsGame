namespace CrapsGame.Core;

/// <summary>
/// Фази раунду в грі Craps.
/// </summary>
public enum GamePhase
{
    /// <summary>
    /// Початкова фаза раунду (перший кидок кубиків).
    /// </summary>
    ComeOut,
    
    /// <summary>
    /// Фаза встановленого очка (повторні кидки до випадіння Point або 7).
    /// </summary>
    Point,
}

/// <summary>
/// Типи ставок, доступні гравцеві на столі для гри в Craps.
/// Включають лінійні ставки, ставки на один кидок (One-Roll) та тривалі ставки (Multi-Roll).
/// </summary>
public enum BetType
{
    // ЛІНІЙНІ СТАВКИ (LINE BETS)

    /// <summary>
    /// Ставка на лінію Pass (на перемогу стрільця). Базова ставка у грі.
    /// <para>Фаза Come-Out: виграє при 7 або 11; програє при 2, 3 або 12.</para>
    /// <para>Фаза Point: виграє, якщо число-очко (Point) випадає раніше за 7; програє, якщо 7 випадає першою (Seven-Out).</para>
    /// </summary>
    PassLine,

    /// <summary>
    /// Ставка на лінію Don't Pass (проти стрільця, на перемогу казино). 
    /// <para>Фаза Come-Out: виграє при 2 або 3; програє при 7 або 11; при 12 зазвичай оголошується нічия (Push).</para>
    /// <para>Фаза Point: виграє, якщо 7 випадає раніше за встановлене очко (Point); програє, якщо очко випадає раніше за 7.</para>
    /// </summary>
    DontPassLine,
    
    // СТАВКИ НА ОДИН КИДОК (ONE-ROLL BETS)
    
    /// <summary>
    /// Ставка "Поле" (Field Bet). Діє рівно на один наступний кидок.
    /// <para>Виграє, якщо сума кубиків дорівнює: 2, 3, 4, 9, 10, 11 або 12.</para>
    /// <para>Програє, якщо випадає: 5, 6, 7 або 8.</para>
    /// </summary>
    Field,

    /// <summary>
    /// Ставка "Any Craps". Діє на один наступний кидок.
    /// <para>Виграє, якщо випадає будь-яке число Craps (сума 2, 3 або 12).</para>
    /// <para>Програє при будь-якому іншому результаті.</para>
    /// </summary>
    AnyCraps,

    /// <summary>
    /// Ставка "Any Seven" (або Big Red). Діє на один наступний кидок.
    /// <para>Виграє, якщо сума кубиків дорівнює точно 7.</para>
    /// <para>Програє при будь-якому іншому числі.</para>
    /// </summary>
    AnySeven,

    /// <summary>
    /// Ставка "Yo-leven" (або просто Yo). Діє на один наступний кидок.
    /// <para>Виграє, якщо сума кубиків дорівнює точно 11.</para>
    /// <para>Програє при будь-якому іншому числі.</para>
    /// </summary>
    YoLeven,
    
    // ТРИВАЛІ СТАВКИ (MULTI-ROLL / HARDWAYS)
    // Ставки на те, що дубль ("жорстке" число) випаде раніше, ніж "м'яке" поєднання цього ж числа або сімка.

    /// <summary>
    /// Ставка "Hardway 4" (Жорстка четвірка).
    /// <para>Виграє, якщо випадає дубль двійок (2 + 2).</para>
    /// <para>Програє, якщо випадає сімка (7) або "м'яка" четвірка (1 + 3).</para>
    /// </summary>
    Hardway4,

    /// <summary>
    /// Ставка "Hardway 6" (Жорстка шістка).
    /// <para>Виграє, якщо випадає дубль трійок (3 + 3).</para>
    /// <para>Програє, якщо випадає сімка (7) або "м'яка" шістка (1 + 5, 2 + 4).</para>
    /// </summary>
    Hardway6,

    /// <summary>
    /// Ставка "Hardway 8" (Жорстка вісімка).
    /// <para>Виграє, якщо випадає дубль четвірок (4 + 4).</para>
    /// <para>Програє, якщо випадає сімка (7) або "м'яка" вісімка (2 + 6, 3 + 5).</para>
    /// </summary>
    Hardway8,

    /// <summary>
    /// Ставка "Hardway 10" (Жорстка десятка).
    /// <para>Виграє, якщо випадає дубль п'ятірок (5 + 5).</para>
    /// <para>Програє, якщо випадає сімка (7) або "м'яка" десятка (4 + 6).</para>
    /// </summary>
    Hardway10
}

/// <summary>
/// Поточний статус або фінальний результат конкретної ставки після кидка кубиків.
/// </summary>
public enum BetOutcome
{
    /// <summary>
    /// Ставка все ще залишається на столі ("активна"). Результат не визначено, 
    /// гравець чекає на наступні кидки (характерно для PassLine або Hardways).
    /// </summary>
    InProgress,

    /// <summary>
    /// Ставка успішно зіграла. Гравець отримує суму своєї ставки назад + чистий виграш 
    /// відповідно до коефіцієнтів виплат (Payout Odds).
    /// </summary>
    Win,

    /// <summary>
    /// Ставка програла. Фішки гравця переходять у дохід казино, а сама ставка 
    /// видаляється з ігрового столу.
    /// </summary>
    Lose
}